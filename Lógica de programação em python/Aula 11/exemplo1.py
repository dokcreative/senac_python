# Criação de matriz 2x2
# A matriz é feita de linhas e colunas (matriz bidimensional), uma estrutura de listas aninhadas.
matriz = [
    ["Caio", "Maria"],
    ["Lorena", "Julia"]
]

# printar posiçao dos dados dentro da matriz
a = matriz [0][0]
print(a)

# Impressão da matriz
print("Matriz:")
for percorrer in matriz:
    print(percorrer)

